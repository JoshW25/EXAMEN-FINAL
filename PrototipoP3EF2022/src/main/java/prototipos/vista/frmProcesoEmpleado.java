    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID ");
        modelo.addColumn("P.NOMBRE");
        modelo.addColumn("S.NOMBRE");
        modelo.addColumn("P.APELLIDO");
        modelo.addColumn("S.APELLIDO");
        modelo.addColumn("CORREO");
        modelo.addColumn("PUESTO");
        modelo.addColumn("DIRECCION");
        modelo.addColumn("FECHA");
        modelo.addColumn("NIT");
        modelo.addColumn("DPI");
        daoEmpleado productoDAO = new daoEmpleado();
        List<clsEmpleado> producto = productoDAO.select();
        tablaVendedores.setModel(modelo);
        String[] dato = new String[11];
        for (int i = 0; i < producto.size(); i++) {
            dato[0] = Integer.toString(producto.get(i).getId());
            dato[1] = producto.get(i).getNombre1();
            dato[2] = producto.get(i).getNombre2();
            dato[3] = producto.get(i).getApellido1();
            dato[4] = producto.get(i).getApellido2();
            dato[5] = producto.get(i).getCorreo();
            dato[6] = Integer.toString(producto.get(i).getPuesto());
            dato[7] = producto.get(i).getDireccion();
            dato[8] = producto.get(i).getFecha();
            dato[9] = producto.get(i).getNit();
            dato[10] = producto.get(i).getDpi();
            
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

