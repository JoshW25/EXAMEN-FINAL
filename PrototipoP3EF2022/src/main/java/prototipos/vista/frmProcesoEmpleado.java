    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        daoEmpleado productoDAO = new daoEmpleado();
        clsEmpleado productoAInsertar = new clsEmpleado();
        productoAInsertar.setId(Integer.parseInt(txtEntrenador.getText()));
        productoAInsertar.setNombre1(txtNombre.getText());
        productoAInsertar.setNombre2(txtNombre2.getText());
        productoAInsertar.setApellido2(txtApellido2.getText());
        productoAInsertar.setApellido1(txtApellido.getText());
        productoAInsertar.setCorreo(txtCorreo.getText());
        productoAInsertar.setPuesto(Integer.parseInt(String.valueOf(cbxPuesto.getSelectedItem())));
        productoAInsertar.setDireccion(txtDireccion.getText());
        String dia = Integer.toString(jdFecha.getCalendar().get(Calendar.DAY_OF_MONTH));
        String mes = Integer.toString(jdFecha.getCalendar().get(Calendar.MONTH) + 1);
        String year = Integer.toString(jdFecha.getCalendar().get(Calendar.YEAR));
        String fecha = (year + "-" + mes + "-" + dia);
        System.out.println(fecha);
        productoAInsertar.setFecha(fecha);
        productoAInsertar.setNit(txtNit.getText());
        productoAInsertar.setDpi(txtDpi.getText());       
        productoDAO.insert(productoAInsertar);
        llenadoDeTablas();
    }//GEN-LAST:event_btnRegistrarActionPerformed

